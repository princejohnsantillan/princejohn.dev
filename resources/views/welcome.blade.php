            <div class="mt-10 max-w-2xl">
                <h2 class="text-2xl font-bold tracking-tight text-gray-900">Work! Work. Work?</h2>
                <p class="mt-4 leading-8"><em>Let's start with <strong>Work?</strong></em><br />So this year a college
                    friend and I started a Baby Diapers business. We had a Shopify store and Facebook for our online
                    channels. We were working on pairing with hospitals to include our diapers as part of their newborn
                    kits. And we were trying to find resellers and sub distributors. We started off the business with
                    excitement, but as months passed my wife and I realized this just wasn't working for us. Many times,
                    we didn't know what we were doing. Warehousing and logistics were difficult! Finding a reliable
                    delivery rider was tough. Our last rider stole a few days worth of cash-on-delivery collection.
                    Resolving issues with customers was draining and sometimes frustrating. I think we could have
                    figured all these issues out if we stuck with it, but given our family's current situation, we
                    couldn't. We were more focused with other areas of our life. So we decided to part ways with my
                    college friend, and gave him all of our company shares as we exited that business.</p>
                <p class="mt-4 leading-8"><em>Then there's <strong>work</strong> at my day job.</em><br />My day job
                    was
                    something I really enjoyed. I loved the people I work with and I love the things we're building. Q1
                    this year, the company underwent a merger and upper management when through an overhaul.
                    Unfortunately, company changes involved a bunch of layoffs. I was asked to select a few members from
                    my team to let go. It was a tough decision and I had to break the news to them myself. As a team
                    lead, it's heartbreaking to let go of people you love working with and who did no wrong. Since the
                    layoffs, the team engagement dropped the the enthusiasm to work flatlined. From there moving
                    forward, everybody was just going thru the motions. For the rest of the year, there was a hovering
                    feeling of job insecurity. To make a long story short, the company eventually shut down the entire
                    Philippine team in Q4 this year. </p>
