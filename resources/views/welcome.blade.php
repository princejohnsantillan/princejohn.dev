                    this way. Their discipline and motivation seem more intrinsic: take Stephen Curry and Taylor Swift,
                    for example. And you know what? So far, so good; we're happy with what we're seeing from our
                    daughter—the habits and manners she's picking up from school have been great! She loves going to
                    school, she loves her classmates and her teachers, she's always excited for show and tell. I feel
                    like we made the right move.</p>
